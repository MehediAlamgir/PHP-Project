		<h1 align = "center">AIUB DRAMA CLUB (ADC): RINGING IN THE MEMBERS ORIENTATION SPRING "13-14" WITH "OSHOMAPTO SHOPNER ARTONAD" </h1>
		<p class="mylabel">
			Opening the gates to a new generation of members, AIUB Drama Club [ADC], organized their Member Orientation program for Spring "13-14". On the 10th of February 14, 
			the new members were given a preview of what it means to be a part of the club through the welcome speech by the General Secretary of ADC, Alimah Islam Asha. Following 
			the introduction session, ADC staged their new production of “Oshomapto Shopner Artonad”, a play written and directed by Ahsah Habib Akash. The drama based on the
			 Liberation War back in 1971, represented the history of Bangladesh, from its inception to the changes it brought in the scenario today, focusing how much was accomplished through 
			 the passage of time. The play left behind a message for the youth of today, urging them to realize the responsibility that was passed down to them through generations of carrying 
			 forward the dream of an independent and prosperous Bangladesh. Upholding the tradition, as a token of their acceptance, all the new members were awarded ‘YES’ cards, followed by
			 speeches from the attending guests, who shared their stories and experiences with ADC. The president of ADC ended the program with his closing speech, after which the club looked
			 forward to beginning a new journey in this new era with their new members and their newfound ‘passion for creation’.
			 
